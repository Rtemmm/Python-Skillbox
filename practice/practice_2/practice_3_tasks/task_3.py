shop = [['каретка', 1200], ['шатун', 1000],
        ['седло', 300], ['педаль', 100],
        ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]

answer = input("Название детали: ")
n = int(input("Количество деталей: "))

sum = 0

for item in shop:
    if n == 0:
        break
    if item[0] == answer:
        sum += item[1]
        n -= 1

print('Общая стоимость: ', sum)
